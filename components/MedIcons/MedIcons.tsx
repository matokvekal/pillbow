// Get icon component by shape ID
export const getMedIcon = (shapeId: string): React.FC<IconProps> => {
  return MedIconMap[shapeId] || PillIcon;
};

// Render icon as JSX with props
interface MedIconProps extends IconProps {
  shapeId: string;
}

export const MedIcon: React.FC<MedIconProps> = ({ shapeId, size = 24, color = 'currentColor', className }) => {
  const IconComponent = getMedIcon(shapeId);
  return <IconComponent size={size} color={color} className={className} />;
};

export default MedIcon;
